    void load(){
        loadCboYear(myService.showYear());
        
        int y = currentDate.getYear();        
        int m = currentDate.getMonthValue();
        updatePieChart(y, m);
        updatePieChartTop(y, m);
        updateDonutChart(y, m);
    }
    
    void loadCboYear(ArrayList<Integer> ls){
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboYear.getModel();
        model.removeAllElements();
        for (Integer i : ls) {
            model.addElement(i);
        }
    }
    
    void loadCboMonth(ArrayList<Integer> ls){
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboMonth.getModel();
        model.removeAllElements();
        for (Integer i : ls) {
            model.addElement(i);
        }
    }

    private Color getColor(int i){
        Color[] color = new Color[]{new Color(248, 113, 113),new Color(251, 146, 60),new Color(251, 191, 37),
                                        new Color(163, 230, 53),new Color(51, 211, 153),new Color(73, 45, 150)};
        return color[i];
    }
    
    //LOAD
    void updatePieChart(int year, int month) {
        ArrayList<PieChartViewModel> bestSellers = myService.getListBestSeller(year, month);
        int i = 0;
        for (PieChartViewModel model : bestSellers) {
            pcBestSellerM.addData(new PieChartModel(model.getTenSP(), model.getSoL(), getColor(i++))); 
        }
    }
    void updatePieChartTop(int year, int month) {
        ArrayList<PieChartViewModel> topDoanhMuc = myService.getTopSell(year, month);
        int i = 0;
        for (PieChartViewModel model : topDoanhMuc) {
            pcBestDM.addData(new PieChartModel(model.getTenDM(), model.getSoL(), getColor(i++)));
        }
    }
    void updateDonutChart(int year, int month) {
        ArrayList<DonutPieChartViewModel> topProfit = myService.getTopProfit(year, month);
        int i = 0;
        for (DonutPieChartViewModel model : topProfit) {
            donutProfit.addData(new PieChartModel(model.getTenSP(), model.getLoiN(), getColor(i++)) );
        }
    }

