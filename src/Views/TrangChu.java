    private void cboYearMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cboYearMouseClicked
        // YEAR
        if (cboYear.getSelectedIndex() >=0 ) {
            int y = Integer.valueOf(cboYear.getSelectedItem().toString());
            try {
                loadCboMonth(myService.showMonth(y));
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_cboYearMouseClicked

    private void cboMonthMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cboMonthMouseClicked
        // MONTH
        if (cboMonth.getSelectedIndex()>=0) {
            pcBestSellerM.clearData();
            pcBestDM.clearData();
            donutProfit.clearData();
            int y = Integer.valueOf(cboYear.getSelectedItem().toString());
            int m = Integer.valueOf(cboMonth.getSelectedItem().toString());
            updatePieChart(y, m);
            updatePieChartTop(y, m);
            updateDonutChart(y, m);
        }
    }//GEN-LAST:event_cboMonthMouseClicked


