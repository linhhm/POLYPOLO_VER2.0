public class SP_Search {
    private String tatCa, nhanH, mauS, chatL, kichC;

    public SP_Search() {
    }

    public SP_Search(String tatCa, String nhanH, String mauS, String chatL, String kichC) {
        this.tatCa = tatCa;
        this.nhanH = nhanH;
        this.mauS = mauS;
        this.chatL = chatL;
        this.kichC = kichC;
    }

    public String getTatCa() {
        return tatCa;
    }

    public void setTatCa(String tatCa) {
        this.tatCa = tatCa;
    }

    public String getNhanH() {
        return nhanH;
    }

    public void setNhanH(String nhanH) {
        this.nhanH = nhanH;
    }

    public String getMauS() {
        return mauS;
    }

    public void setMauS(String mauS) {
        this.mauS = mauS;
    }

    public String getChatL() {
        return chatL;
    }

    public void setChatL(String chatL) {
        this.chatL = chatL;
    }

    public String getKichC() {
        return kichC;
    }

    public void setKichC(String kichC) {
        this.kichC = kichC;
    }
    
    
    
}
